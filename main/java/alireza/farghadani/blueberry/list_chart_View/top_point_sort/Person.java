package alireza.farghadani.blueberry.list_chart_View.top_point_sort;

public class Person {
    private final String name;
    private final float point;

    public Person(String name, float point) {
        this.name = name;
        this.point = point;
    }

    public String getName() {
        return name;
    }

    public float getPoint() {
        return point;
    }
}